public class Array{
    
    public static int sumaDiagonal(int[][] args){
        
        int este=0;
        
        for(int i=0;i<args.length;i++){
            
            for(int j=0;j<args[i].length;j++){
                
                if(i==j){
                    
                    este += args[i][j];
}
   }
}

return este;
        }
        
    }
